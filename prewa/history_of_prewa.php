 <p style="text-align:justify;line-height: 32px">For the purpose of creating opportunities for communication and welfare among themselves after retirement Engineer Muhammad Harunur Rashid took the initiative and discussed  the matter with Mr. Ahbab Hussain Chowdhury along with Engineer Md. Shefaul Alam and Engineer Khondker Salahuddin and they also showed interest on that. Then on 19-04-2015 the first meeting of retired Engineers of PWD was held at Purta Bhaban. 44 Engineers were present at the meeting. In the meeting a seven member adhoc committee was formed with Engineer Ahbab Hussain Chowdhury as Convenor. The Adhoc  Committee was given the responsibility to draft a Constitution. In a follow up meeting held on 18-06-2015, the adhoc Committee proposed a draft Constitution and it was approved by the Engineers present with some minor modifications. The meeting also took decision that the Adhoc  Committee will continue its functions until a Committee is formed in the next Annual General Meeting. Then on 31-01-2018 the first general meeting was held at Ramna Park Restaurant. The first executive committee was formed as per the constitution in the presence of 74 members at the meeting.</p>

